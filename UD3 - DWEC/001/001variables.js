/* 001variables.js/.html: Declara dos variables: admin y name.
Asigna el valor "John Snow" a name.
Copia el valor de name a admin.
Muestra el valor de admin usando alert (debe salir “John Snow”). */

"use strict";

//Aqui se declaran las variables
let admin;
let name;

//Se le asigna un nombre a la variable name
name = "John Snow";

//Decimos que el contenido que va a tener admin es el que tiene name
admin = name;

//Se muuestra en la página una alerta que muestra el contenido de admin
alert(admin);